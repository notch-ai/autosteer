export const EmojiPicker: React.FC<EmojiPickerProps> = ({ onEmojiSelect, trigger, className }) => {
  const [search, setSearch] = useState('');
  const [selectedCategory, setSelectedCategory] = useState('Smileys & Emotion');

  const filteredEmojis = search
    ? Object.values(emojiCategories)
        .flat()
        .filter((emoji) => emoji.toLowerCase().includes(search.toLowerCase()))
    : emojiCategories[selectedCategory as keyof typeof emojiCategories] || [];

  return (
    <Popover>
      <PopoverTrigger asChild>
        {trigger || (
          <Button variant="ghost" size="icon" className="h-8 w-8">
            😊
          </Button>
        )}
      </PopoverTrigger>
      <PopoverContent className={cn('w-80 p-0', className)}>
        <div className="p-2 border-b border-border">
          <Input
            placeholder="Search emoji..."
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            className="h-8"
          />
        </div>
        {!search && (
          <div className="flex gap-1 p-2 border-b border-border overflow-x-auto">
            {Object.keys(emojiCategories).map((category) => (
              <Button
                key={category}
                variant={selectedCategory === category ? 'secondary' : 'ghost'}
                size="sm"
                onClick={() => setSelectedCategory(category)}
                className="flex-shrink-0"
              >
                {category.split(' ')[0]}
              </Button>
            ))}
          </div>
        )}
        <ScrollArea className="h-64">
          <div className="grid grid-cols-8 gap-1 p-2">
            {filteredEmojis.map((emoji, index) => (
              <Button
                key={index}
                variant="ghost"
                size="icon"
                onClick={() => onEmojiSelect(emoji)}
                className="h-8 w-8 text-lg"
              >
                {emoji}
              </Button>
            ))}
          </div>
        </ScrollArea>
      </PopoverContent>
    </Popover>
  );
};
